#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*last;
	char	ch;

	last = NULL;
	ch = (char)c;
	while (1)
	{
		if (*s == ch)
			last = (char *)s;
		if (*s == '\0')
			break ;
		s++;
	}
	return (last);
}
